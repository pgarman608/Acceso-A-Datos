    private void jb_Fichero1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_Fichero1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fileChooser.setFileFilter(this.filtrotxt);
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            this.jl_fin.setText(" ");
            this.jtf_Fichero1.setText(fileChooser.getSelectedFile().getAbsolutePath());
            this.jtf_Fichero2.setEditable(true);
            this.jb_Fichero2.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(this,"No ha encontrado el fichero");
        }
    }//GEN-LAST:event_jb_Fichero1ActionPerformed

