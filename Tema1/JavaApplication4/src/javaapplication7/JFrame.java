    public String toS(FileReader file) throws IOException{
        int aux1 = 1;
        String aux2 = "";
        while(aux1 >= 0){
            aux1 = file.read();
            aux2 += (char)aux1;
            System.out.println(aux2);
        }
        file.close();
        return aux2;
    }
