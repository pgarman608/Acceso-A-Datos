    private void jb_comprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_comprobarActionPerformed
        this.jl_fin.setText(" ");
        File f1 = new File(this.jtf_Fichero1.getText());
        File f2 = new File(this.jtf_Fichero2.getText());
        FileReader fr1;
        FileReader fr2;
        String fr1String;
        String fr2String;
        
        if (f1.exists() && f2.exists()) {
            try {
                fr1 = new FileReader(f1);
                fr2 = new FileReader(f2);
                fr1String= this.toS(fr1);
                fr2String= this.toS(fr2);
                if (fr1String.equals(fr2String)) {
                    this.jl_fin.setText("Son iguales");
                }else{
                    this.jl_fin.setText("Son diferentes");
                }
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "No se ha encontrado el fichero");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error a la hora de leer el fichero");
            }
            
        }else{
            if (f1.exists()) {
                JOptionPane.showMessageDialog(this,"No ha encontrado el fichero 1");
            }else{
                JOptionPane.showMessageDialog(this,"No ha encontrado el fichero 2");
            }
        }
    }//GEN-LAST:event_jb_comprobarActionPerformed

    
